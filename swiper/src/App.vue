<script>
    export default{
        name: 'app',
        data() {
            return {transitionName: ''}
        },
        watch: {
            '$route'(to, from) {
                const toDepth = to.path.split('/').length;
                const fromDepth = from.path.split('/').length;
                if (to.name == 'user' && from.name == 'index') {
                    this.transitionName = 'slide-left';
                } else if (to.name == 'address' && from.name == 'user') {
                    this.transitionName = 'slide-left';
                } else if (to.name == 'shop' && from.name == 'goods') {
                    this.transitionName = 'slide-left';
                }else if (to.name == 'goods' && from.name == 'shop') {
                    this.transitionName = 'slide-right';
                }else if (to.name == 'address-add' && from.name == 'address-choose') {
                    this.transitionName = 'slide-left';
                }else if (to.name == 'order' && from.name == 'user') {
                    this.transitionName = 'slide-left';
                }
                else {
                    this.transitionName = toDepth > fromDepth ? 'slide-left' : 'slide-right';
                }
            }
        }
    }
</script>
